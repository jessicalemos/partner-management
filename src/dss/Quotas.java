    public void addRowQToJTable(){
        model =  (DefaultTableModel) jTable1.getModel();
        int n = (Integer) novo.model.getValueAt(novo.row,0);
        List<Quota> q = novo.socios.getSocios().get(n).getQuotas();
        Object rowData[] = new Object[2];
        for(Quota a: q){
            rowData[0] = a.getData();
            rowData[1] = a.getValor();
            model.addRow(rowData);
        }
    }

